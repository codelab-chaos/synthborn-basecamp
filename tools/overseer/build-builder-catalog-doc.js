#!/usr/bin/env node
// Render the curated SynthOverseer BuilderTools catalog as a markdown reference doc.
//
// Reads mods/SynthOverseer/src/main/resources/synthoverseer/builder-commands.json
// (the source of truth used by RunBuilderCommandTool + ListBuilderCommandsTool) and emits
// docs/synthoverseer-builder-commands.md — a per-command reference with descriptions,
// param tables, tag chips, and example arg dicts.
//
// Usage:
//   node tools/overseer/build-builder-catalog-doc.js
//
// Re-run after editing builder-commands.json so the doc stays in sync.

const fs = require('fs');
const path = require('path');

const { modDir } = require('../library/workspace');

const REPO_ROOT = path.resolve(__dirname, '..', '..');
const INPUT = path.join(modDir('SynthOverseer'), 'src', 'main', 'resources',
    'synthoverseer', 'builder-commands.json');
const OUTPUT = path.join(REPO_ROOT, 'docs', 'synthoverseer-builder-commands.md');

function render(catalog) {
    const cmds = catalog.commands || [];
    const byCategory = new Map();
    for (const c of cmds) {
        if (!byCategory.has(c.category)) byCategory.set(c.category, []);
        byCategory.get(c.category).push(c);
    }
    const categoryOrder = ['selection', 'write', 'clipboard', 'transform', 'history', 'shape'];
    const sortedCats = categoryOrder.filter(c => byCategory.has(c));
    for (const c of byCategory.keys()) {
        if (!sortedCats.includes(c)) sortedCats.push(c);
    }

    const lines = [];
    lines.push('# SynthOverseer — BuilderTools Catalog');
    lines.push('');
    lines.push('Auto-generated by [tools/overseer/build-builder-catalog-doc.js](../tools/overseer/build-builder-catalog-doc.js).');
    lines.push('Source of truth: [builder-commands.json](../mods/SynthOverseer/src/main/resources/synthoverseer/builder-commands.json).');
    lines.push('');
    lines.push(`Catalog version **${catalog.version}** — ${cmds.length} curated commands. The Overseer dispatches these via the \`run_builder_command\` tool; the LLM browses them via \`list_builder_commands\`. All effects join Hytale's BuilderTools undo stack, so \`/undo\` (or \`/osu\`) reverts. Permission: \`hytale:WorldEditor\`.`);
    lines.push('');
    if (catalog._ui_safety_policy) {
        lines.push('## UI-safety policy');
        lines.push('');
        lines.push(catalog._ui_safety_policy);
        lines.push('');
    }
    if (catalog._tags_glossary) {
        lines.push('## Tag glossary');
        lines.push('');
        lines.push('| Tag | Meaning |');
        lines.push('|---|---|');
        for (const [tag, desc] of Object.entries(catalog._tags_glossary)) {
            lines.push(`| \`${tag}\` | ${desc} |`);
        }
        lines.push('');
    }
    lines.push('## Categories');
    lines.push('');
    for (const cat of sortedCats) {
        lines.push(`- [${cat}](#${cat}) — ${byCategory.get(cat).length}`);
    }
    lines.push('');
    lines.push('---');
    lines.push('');

    for (const cat of sortedCats) {
        lines.push(`## ${cat}`);
        lines.push('');
        const sorted = [...byCategory.get(cat)].sort((a, b) => a.name.localeCompare(b.name));
        for (const c of sorted) {
            lines.push(`### \`/${c.name}\``);
            lines.push('');
            lines.push(c.description);
            lines.push('');
            if (c.tags && c.tags.length > 0) {
                lines.push(`**Tags**: ${c.tags.map(t => '`' + t + '`').join(', ')}`);
                lines.push('');
            }
            if (c.params && c.params.length > 0) {
                lines.push('| Param | Type | Required | Description |');
                lines.push('|---|---|---|---|');
                for (const p of c.params) {
                    lines.push(`| \`${p.name}\` | ${p.type} | ${p.required ? 'yes' : 'no'} | ${p.desc} |`);
                }
                lines.push('');
            } else {
                lines.push('No arguments — pass `args: {}`.');
                lines.push('');
            }
            if (c.examples && c.examples.length > 0) {
                lines.push('**Examples:**');
                lines.push('');
                for (const ex of c.examples) {
                    lines.push('```json');
                    lines.push('{ "name": "' + c.name + '", "args": ' + JSON.stringify(ex) + ' }');
                    lines.push('```');
                }
                lines.push('');
            }
        }
        lines.push('---');
        lines.push('');
    }
    return lines.join('\n');
}

function main() {
    if (!fs.existsSync(INPUT)) {
        console.error(`✗ catalog source not found: ${INPUT}`);
        process.exit(1);
    }
    const catalog = JSON.parse(fs.readFileSync(INPUT, 'utf8'));
    const out = render(catalog);
    fs.writeFileSync(OUTPUT, out, 'utf8');
    console.log(`✓ wrote ${OUTPUT}`);
    console.log(`  ${(catalog.commands || []).length} commands, ${Object.keys(catalog._tags_glossary || {}).length} tag types`);
}

main();
