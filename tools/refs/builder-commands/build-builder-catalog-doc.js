#!/usr/bin/env node
// Render a BuilderTools command catalog JSON file as markdown.
//
// The default input is the SynthOverseer catalog because it is the current curated
// source, but the renderer is intentionally generic.
//
// Usage:
//   node tools/refs/builder-commands/build-builder-catalog-doc.js
//   node tools/refs/builder-commands/build-builder-catalog-doc.js --catalog path/to/builder-commands.json --out docs/builder-commands.md
//
// Re-run after editing the source catalog so the doc stays in sync.

const fs = require('fs');
const path = require('path');

const { modDir } = require('../../lib/workspace');

const REPO_ROOT = path.resolve(__dirname, '..', '..', '..');
const DEFAULT_INPUT = path.join(modDir('SynthOverseer'), 'src', 'main', 'resources',
    'synthoverseer', 'builder-commands.json');
const DEFAULT_OUTPUT = path.join(REPO_ROOT, 'docs', 'synthoverseer-builder-commands.md');

function parseArgs(argv) {
    const opts = { catalog: DEFAULT_INPUT, out: DEFAULT_OUTPUT };
    for (let i = 2; i < argv.length; i += 1) {
        const arg = argv[i];
        const next = () => {
            const value = argv[++i];
            if (!value) throw new Error(`${arg} requires a value`);
            return value;
        };
        if (arg === '--catalog') opts.catalog = path.resolve(next());
        else if (arg === '--out') opts.out = path.resolve(next());
        else if (arg === '-h' || arg === '--help') {
            console.log('Usage: node tools/refs/builder-commands/build-builder-catalog-doc.js [--catalog <file>] [--out <file>]');
            process.exit(0);
        } else {
            throw new Error(`Unknown argument: ${arg}`);
        }
    }
    return opts;
}

function render(catalog) {
    const cmds = catalog.commands || [];
    const byCategory = new Map();
    for (const c of cmds) {
        if (!byCategory.has(c.category)) byCategory.set(c.category, []);
        byCategory.get(c.category).push(c);
    }
    const categoryOrder = ['selection', 'write', 'clipboard', 'transform', 'history', 'shape'];
    const sortedCats = categoryOrder.filter(c => byCategory.has(c));
    for (const c of byCategory.keys()) {
        if (!sortedCats.includes(c)) sortedCats.push(c);
    }

    const lines = [];
    lines.push('# SynthOverseer — BuilderTools Catalog');
    lines.push('');
    lines.push('Auto-generated by [tools/refs/builder-commands/build-builder-catalog-doc.js](../tools/refs/builder-commands/build-builder-catalog-doc.js).');
    lines.push('Source of truth: builder command catalog JSON.');
    lines.push('');
    lines.push(`Catalog version **${catalog.version}** — ${cmds.length} curated commands. The Overseer dispatches these via the \`run_builder_command\` tool; the LLM browses them via \`list_builder_commands\`. All effects join Hytale's BuilderTools undo stack, so \`/undo\` (or \`/osu\`) reverts. Permission: \`hytale:WorldEditor\`.`);
    lines.push('');
    if (catalog._ui_safety_policy) {
        lines.push('## UI-safety policy');
        lines.push('');
        lines.push(catalog._ui_safety_policy);
        lines.push('');
    }
    if (catalog._tags_glossary) {
        lines.push('## Tag glossary');
        lines.push('');
        lines.push('| Tag | Meaning |');
        lines.push('|---|---|');
        for (const [tag, desc] of Object.entries(catalog._tags_glossary)) {
            lines.push(`| \`${tag}\` | ${desc} |`);
        }
        lines.push('');
    }
    lines.push('## Categories');
    lines.push('');
    for (const cat of sortedCats) {
        lines.push(`- [${cat}](#${cat}) — ${byCategory.get(cat).length}`);
    }
    lines.push('');
    lines.push('---');
    lines.push('');

    for (const cat of sortedCats) {
        lines.push(`## ${cat}`);
        lines.push('');
        const sorted = [...byCategory.get(cat)].sort((a, b) => a.name.localeCompare(b.name));
        for (const c of sorted) {
            lines.push(`### \`/${c.name}\``);
            lines.push('');
            lines.push(c.description);
            lines.push('');
            if (c.tags && c.tags.length > 0) {
                lines.push(`**Tags**: ${c.tags.map(t => '`' + t + '`').join(', ')}`);
                lines.push('');
            }
            if (c.params && c.params.length > 0) {
                lines.push('| Param | Type | Required | Description |');
                lines.push('|---|---|---|---|');
                for (const p of c.params) {
                    lines.push(`| \`${p.name}\` | ${p.type} | ${p.required ? 'yes' : 'no'} | ${p.desc} |`);
                }
                lines.push('');
            } else {
                lines.push('No arguments — pass `args: {}`.');
                lines.push('');
            }
            if (c.examples && c.examples.length > 0) {
                lines.push('**Examples:**');
                lines.push('');
                for (const ex of c.examples) {
                    lines.push('```json');
                    lines.push('{ "name": "' + c.name + '", "args": ' + JSON.stringify(ex) + ' }');
                    lines.push('```');
                }
                lines.push('');
            }
        }
        lines.push('---');
        lines.push('');
    }
    return lines.join('\n');
}

function main() {
    const opts = parseArgs(process.argv);
    if (!fs.existsSync(opts.catalog)) {
        console.error(`catalog source not found: ${opts.catalog}`);
        process.exit(1);
    }
    const catalog = JSON.parse(fs.readFileSync(opts.catalog, 'utf8'));
    const out = render(catalog);
    fs.mkdirSync(path.dirname(opts.out), { recursive: true });
    fs.writeFileSync(opts.out, out, 'utf8');
    console.log(`wrote ${opts.out}`);
    console.log(`  ${(catalog.commands || []).length} commands, ${Object.keys(catalog._tags_glossary || {}).length} tag types`);
}

main();
